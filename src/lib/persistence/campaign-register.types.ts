function register<const TDefinition extends Omit<CampaignRegisterDefinition, "fields"> & {
  fields?: readonly CampaignFieldDefinition[];
}>(
  definition: TDefinition,
): Omit<TDefinition, "fields"> & CampaignRegisterDefinition {
  return {
    ...definition,
    fields: [
      baseFields.businessId,
      baseFields.title,
      { ...baseFields.type, options: definition.typeOptions },
      { ...baseFields.status, options: definition.statusOptions },
      ...(definition.fields ?? []),
      baseFields.summary,
      baseFields.evidenceReference,
      baseFields.notes,
    ],
  } as Omit<TDefinition, "fields"> & CampaignRegisterDefinition;
}

