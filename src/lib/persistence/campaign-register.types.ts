import type { LifecycleMetadata } from "./lifecycle.types";

export type CampaignFieldType = "text" | "textarea" | "select" | "multiselect" | "checkbox";
export type CampaignFieldRelationName =
  | "locations"
  | "processes"
  | "equipment"
  | "chemicals"
  | "hazards"
  | "controls"
  | "riskAssessments"
  | "segs"
  | "findings"
  | "incidents"
  | "complianceItems"
  | "correctiveActions";
export type CampaignFieldRelation = CampaignFieldRelationName | string;

export interface CampaignFieldDefinition {
  name: string;
  label: string;
  type?: CampaignFieldType;
  required?: boolean;
  options?: readonly string[];
  relation?: CampaignFieldRelation;
  defaultValue?: string | boolean | string[];
  rows?: number;
  placeholder?: string;
  helperText?: string;
}

export interface CampaignRegisterDefinition {
  kind: string;
  collection: string;
  basePath: string;
  breadcrumbs: string;
  sidebarSection: string;
  sidebarTitle: string;
  title: string;
  recordLabel: string;
  pluralRecordLabel: string;
  summary: string;
  businessPrefix: string;
  typeOptions: readonly string[];
  statusOptions: readonly string[];
  defaultType: string;
  defaultStatus: string;
  fields: readonly CampaignFieldDefinition[];
  seed?: Record<string, unknown>;
}

